function profile = ResolveChannelProfile(channelModel, delaySpread)
%RESOLVECHANNELPROFILE Return the tap-delay profile used by the channel layer.

    profile = struct();
    profile.name = upper(channelModel);
    profile.los_tap_index = [];
    profile.los_k_linear = [];
    profile.tap_delays_norm = [];
    profile.power_dB = [];

    switch upper(channelModel)
        case 'TDL-A'
            profile.tap_delays_norm = [ ...
                0.0000, 0.3819, 0.4025, 0.5868, 0.4610, 0.5375, 0.6708, 0.5750, 0.7618, ...
                1.5375, 1.8978, 2.2242, 2.1718, 2.4942, 2.5119, 3.0582, 4.0810, 4.4579, ...
                4.5695, 4.7966, 5.0066, 5.3043, 9.6586];
            profile.power_dB = [ ...
               -13.4, 0.0, -2.2, -4.0, -6.0, -8.2, -9.9, -10.5, -7.5, ...
               -15.9, -6.6, -16.7, -12.4, -15.2, -10.8, -11.3, -12.7, -16.2, ...
               -18.3, -18.9, -16.6, -19.9, -29.7];

        case 'TDL-B'
            profile.tap_delays_norm = [ ...
                0.0000, 0.1072, 0.2155, 0.2095, 0.2870, 0.2986, 0.3752, 0.5055, ...
                0.3681, 0.3697, 0.5700, 0.5283, 1.1021, 1.2756, 1.5474, 1.7842, ...
                2.0169, 2.8294, 3.0219, 3.6187, 4.1067, 4.2790, 4.7834];
            profile.power_dB = [ ...
                0.0, -2.2, -4.0, -3.2, -9.8, -1.2, -3.4, -5.2, -7.6, -3.0, ...
                -8.9, -9.0, -4.8, -5.7, -7.5, -1.9, -7.6, -12.2, -9.8, -11.4, ...
                -14.9, -9.2, -11.3];

        case 'TDL-C'
            profile.tap_delays_norm = [ ...
                0.0000, 0.2099, 0.2219, 0.2329, 0.2176, 0.6366, 0.6448, 0.6560, ...
                0.6584, 0.7935, 0.8213, 0.9336, 1.2285, 1.3083, 2.1704, 2.7105, ...
                4.2589, 4.6003, 5.4902, 5.6077, 6.3065, 6.6374, 7.0427, 8.6523];
            profile.power_dB = [ ...
                -4.4, -1.2, -3.5, -5.2, -2.5, 0.0, -2.2, -3.9, -7.4, -7.1, ...
                -10.7, -11.1, -5.1, -6.8, -8.7, -13.2, -13.9, -13.9, -15.8, ...
                -17.1, -16.0, -15.7, -21.6, -22.8];

        case 'TDL-D'
            profile.tap_delays_norm = [ ...
                0.0000, 0.0000, 0.0350, 0.6120, 1.3630, 1.4050, 1.8040, 2.5960, ...
                1.7750, 4.0420, 7.9370, 9.4240, 9.7080, 12.5250];
            profile.power_dB = [ ...
                -0.2, -13.5, -18.8, -21.0, -22.8, -17.9, -20.1, -21.9, ...
                -22.9, -27.8, -23.6, -24.8, -30.0, -27.7];
            profile.los_tap_index = 1;
            profile.los_k_linear = 10^(13.3 / 10);

        case 'DYNAMIC-RAYLEIGH'
            profile.tap_delays_norm = [0.0000, 0.1800, 0.4300, 0.8500, 1.5500, 2.6000, 4.1000, 6.5000];
            profile.power_dB = [0.0, -1.8, -3.1, -5.4, -7.8, -10.7, -14.1, -18.8];

        case 'DYNAMIC-RICIAN'
            profile.tap_delays_norm = [0.0000, 0.1200, 0.3600, 0.7200, 1.2000, 1.9500, 3.1000, 5.0000];
            profile.power_dB = [0.0, -2.0, -3.4, -5.8, -8.4, -11.3, -15.2, -19.8];
            profile.los_tap_index = 1;
            profile.los_k_linear = [];

        otherwise
            error('不支持的信道配置: %s', channelModel);
    end

    if ~isempty(profile.power_dB)
        tapPowers = 10.^(profile.power_dB / 10);
        profile.tap_powers = tapPowers / sum(tapPowers);
    else
        profile.tap_powers = [];
    end
    profile.tap_delays_s = profile.tap_delays_norm .* delaySpread;
end
